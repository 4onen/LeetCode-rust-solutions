pub struct Solution;

impl Solution {
    pub fn count_fair_pairs(mut nums: Vec<i32>, lower: i32, upper: i32) -> i64 {
        nums.sort_unstable();
        let mut res = 0;
        for i in 0..nums.len() {
            let num = nums[i];
            let tgt_low = lower - num;
            let tgt_up = upper - num;
            let start = nums[0..i].partition_point(|&n| n < tgt_low);
            let end = nums[0..i].partition_point(|&n| n <= tgt_up);
            res += (end - start) as i64;
        }
        res
    }
}

