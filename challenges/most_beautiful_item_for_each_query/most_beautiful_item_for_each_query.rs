// Initial sol'n
impl Solution {
    pub fn maximum_beauty(items: Vec<Vec<i32>>, mut queries: Vec<i32>) -> Vec<i32> {
        // Drain the queries vector so we can reuse its memory for our answers
        let mut sorted_queries: Vec<_> = queries.drain(..).enumerate().collect();
        sorted_queries.sort_unstable_by_key(|&(_, x)| std::cmp::Reverse(x));
        let mut query_answers = queries;
        query_answers.resize(sorted_queries.len(), 0);
        let top_query_price = sorted_queries.first().unwrap().1;
        let mut heap: std::collections::BinaryHeap<(i32, i32)> = items
            .into_iter()
            .filter(|item| item[0] <= top_query_price)
            .map(|item| (item[1], item[0]))
            .collect();
        'outer: for (query_idx, query) in sorted_queries {
            loop {
                match heap.peek() {
                    None => break 'outer,
                    Some(&(_, price)) if price <= query => break,
                    _ => {
                        heap.pop();
                    }
                }
            }
            let top = heap.peek().unwrap();
            // Safe because if the heap was empty, we'd break 'outer.
            query_answers[query_idx] = top.0;
        }
        query_answers
    }
}

