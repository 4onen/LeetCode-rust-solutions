// https://leetcode.com/problems/shortest-subarray-to-be-removed-to-make-array-sorted/

pub struct Solution;

impl Solution {
    pub fn find_length_of_shortest_subarray(arr: Vec<i32>) -> i32 {
        const fn longest_increasing_prefix(arr: &[i32]) -> u32 {
            let mut i = 0;
            while i + 1 < arr.len() as u32 && arr[i as usize] <= arr[i as usize + 1] {
                i += 1;
            }
            i
        }
        const fn longest_increasing_suffix_start(arr: &[i32]) -> u32 {
            // Precondition: arr is _not_ sorted
            let mut i = arr.len() as u32 - 1;
            while arr[i as usize] >= arr[i as usize - 1] {
                i -= 1;
                assert!(i > 0);
            }
            i
        }
        let lip_len = longest_increasing_prefix(&arr);
        if lip_len + 1 >= arr.len() as u32 {
            return 0;
        }
        let mut lis_start = longest_increasing_suffix_start(&arr);
        let mut res = std::cmp::min(arr.len() as u32 - 1 - lip_len, lis_start);
        let mut i = 0;
        while i <= lip_len && lis_start < arr.len() as u32 {
            if arr[i as usize] <= arr[lis_start as usize] {
                res = std::cmp::min(res, lis_start - i - 1);
                i += 1;
            } else {
                lis_start += 1;
            }
        }
        res as i32
    }
}

