// Heap priority rising tide (unstable sort)
impl Solution {
    pub fn max_points(grid: Vec<Vec<i32>>, queries: Vec<i32>) -> Vec<i32> {
        assert!(queries.len() <= 10_000);
        let sorted_queries = {
            let mut sorted_queries = queries
                .iter()
                .enumerate()
                .map(|(i, &q)| (q, i as u16))
                .collect::<Vec<_>>();
            sorted_queries.sort_unstable();
            sorted_queries
        };
        let mut answers = queries; // Re-use the input vector
        let mut seen = vec![vec![false; grid[0].len()]; grid.len()];
        let mut visited = vec![vec![false; grid[0].len()]; grid.len()];
        let mut too_high = std::collections::BinaryHeap::new();
        too_high.push((std::cmp::Reverse(grid[0][0]), 0, 0));
        let mut stack = vec![];
        let mut score = 0;
        for (query, idx) in sorted_queries {
            while !too_high.is_empty() && too_high.peek().unwrap().0 .0 < query {
                let (_, x, y) = too_high.pop().unwrap();
                stack.push((x, y));
                while let Some((x, y)) = stack.pop() {
                    if x < 0
                        || x >= grid.len() as i16
                        || y < 0
                        || y >= grid[0].len() as i16
                        || visited[x as usize][y as usize]
                    {
                        continue;
                    }
                    if grid[x as usize][y as usize] >= query {
                        if !seen[x as usize][y as usize] {
                            seen[x as usize][y as usize] = true;
                            too_high.push((std::cmp::Reverse(grid[x as usize][y as usize]), x, y));
                        }
                        continue;
                    }
                    visited[x as usize][y as usize] = true;
                    score += 1;
                    stack.extend_from_slice(&[(x + 1, y), (x - 1, y), (x, y + 1), (x, y - 1)]);
                }
            }
            answers[idx as usize] = score;
        }
        answers
    }
}

