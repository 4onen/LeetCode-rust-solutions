    fn test(grid: &[&[i32]], queries: &[i32], expected: &[i32]) {
        assert!(grid.len() >= 2);
        assert!(grid.len() <= 1000);
        for &row in grid {
            assert!(row.len() >= 2);
            assert!(row.len() <= 1000);
            assert_eq!(row.len(), grid[0].len());
            assert!(grid.len() * row.len() >= 4);
            assert!(grid.len() * row.len() <= 100_000);
            for &val in row {
                assert!(val >= 1);
                assert!(val <= 1_000_000);
            }
        }
        assert!(queries.len() >= 1);
        assert!(queries.len() <= 10_000);
        for &query in queries {
            assert!(query >= 1);
            assert!(query <= 1_000_000);
        }
        if expected.len() != 0 {
            assert_eq!(expected.len(), queries.len());
        }
        let result =
            Solution::max_points(grid.iter().map(|&x| x.to_vec()).collect(), queries.to_vec());
        if expected.len() > 0 {
            assert_eq!(result, expected);
        }
        // Else, simple do-not-crash test
    }

