// https://leetcode.com/problems/design-an-atm-machine/

const VALUES_CNT: u8 = 5;
const VALUES: [i32; VALUES_CNT as usize] = [20, 50, 100, 200, 500];
pub struct ATM {
    contents: [i32; 5],
}
impl ATM {
    pub fn new() -> Self {
        Self { contents: [0; 5] }
    }
    pub fn deposit(&mut self, banknotes_count: Vec<i32>) {
        for i in 0..VALUES_CNT {
            self.contents[i as usize] += banknotes_count[i as usize];
        }
    }
    pub fn withdraw(&mut self, amount: i32) -> Vec<i32> {
        assert!(amount >= 1);
        let mut amount = amount as i32;
        let mut issued = vec![0i32; VALUES_CNT as usize];
        for i in (0..VALUES_CNT).rev() {
            let needed = std::cmp::min(amount / VALUES[i as usize], self.contents[i as usize]);
            issued[i as usize] += needed as i32;
            amount -= needed * VALUES[i as usize];
        }
        if amount > 0 {
            // Abort transaction
            issued.truncate(1);
            issued[0] = -1;
        } else {
            for i in 0..VALUES_CNT {
                self.contents[i as usize] -= issued[i as usize];
            }
        }
        issued
    }
}

