// Rewrite bad pair equation sol'n
// impl Solution {
//     pub fn count_bad_pairs(nums: Vec<i32>) -> i64 {
//         // Bad condition rewrite: nums[i] - i != nums[j] - j
//         // Good condition: nums[i] - i == nums[j] - j
//         // Total pairs: nums.len() * (nums.len() - 1)/2
//         let mut diffs = std::collections::HashMap::<i32, i32>::new();
//         for i in 0..nums.len() as i32 {
//             *diffs.entry(nums[i as usize] - i).or_insert(0) += 1;
//         }
//         let good_pairs: i64 = diffs
//             .into_values()
//             .map(|x| (x as i64) * (x as i64 - 1) / 2)
//             .sum();
//         let total_elements = nums.len() as i64;
//         let total_pairs = total_elements * (total_elements - 1) / 2;
//         total_pairs - good_pairs
//     }
// }

// Count good, compute bad sol'n (based on fastest)
impl Solution {
    pub fn count_bad_pairs(nums: Vec<i32>) -> i64 {
        let mut diffs = std::collections::HashMap::<i32, i32>::new();
        let mut result = 0;
        for i in 0..nums.len() as i32 {
            let diff = nums[i as usize] - i;
            result += (i - diffs.get(&diff).copied().unwrap_or(0)) as i64;
            *diffs.entry(nums[i as usize] - i).or_insert(0) += 1;
        }
        result
    }
}


