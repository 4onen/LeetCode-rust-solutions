    fn test(edges: &[[i32; 2]], expected: [i32; 2]) {
        assert!(edges.len() >= 3);
        assert!(edges.len() <= 1000);
        let mut seen = std::collections::HashSet::new();
        for &[a, b] in edges {
            assert!(a >= 1);
            assert!(b > a);
            assert!(b <= edges.len() as i32);
            assert!(seen.insert([a, b]));
        }
        {
            let (a, b) = (expected[0], expected[1]);
            assert!(a >= 1);
            assert!(b > a);
            assert!(b <= edges.len() as i32);
            assert!(seen.contains(&[a, b]));
        }
        assert_eq!(
            Solution::find_redundant_connection(edges.iter().map(|&[a, b]| vec![a, b]).collect()),
            expected
        );
    }

