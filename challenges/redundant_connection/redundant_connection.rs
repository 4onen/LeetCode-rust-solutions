// https://leetcode.com/problems/redundant-connection/

pub struct Solution;

// Union find sol'n
impl Solution {
    pub fn find_redundant_connection(edges: Vec<Vec<i32>>) -> Vec<i32> {
        type Idx = u16;
        fn find(union_set: &mut [Idx], mut x: Idx) -> Idx {
            while union_set[x as usize] != x {
                union_set[x as usize] = union_set[union_set[x as usize] as usize];
                x = union_set[x as usize];
            }
            x
        }
        let n = edges.len() as u16;
        let mut union_set = (0..=n).collect::<Vec<_>>();
        for edge in &edges {
            let a = edge[0] as Idx;
            let b = edge[1] as Idx;
            let pa = find(&mut union_set, a);
            let pb = find(&mut union_set, b);
            if pa == pb {
                return edge.clone();
            }
            union_set[pb as usize] = pa;
            union_set[b as usize] = pa;
        }
        unreachable!()
    }
}

