// https://leetcode.com/problems/minimum-cost-to-hire-k-workers/

pub struct Solution;

impl Solution {
    pub fn mincost_to_hire_workers(quality: Vec<i32>, wage: Vec<i32>, k: i32) -> f64 {
        assert!(k > 0);
        let k = k as usize;
        assert!(quality.len() == wage.len());
        assert!(quality.len() > 0);
        assert!(quality.len() <= 10000);
        assert!(k <= quality.len());
        let mut workers: std::vec::Vec<(f64, u16)> = (0..quality.len() as u16)
            .map(|i| {
                let q = quality[i as usize];
                assert!(q > 0);
                assert!(q <= 10000);
                let w = wage[i as usize];
                (w as f64 / q as f64, q as u16)
            })
            .collect();
        workers.sort_unstable_by(|(a, _), (b, _)| a.partial_cmp(&b).unwrap());
        let mut quality_sum: i32 = 0;
        let mut heap: std::collections::BinaryHeap<u16> = std::collections::BinaryHeap::new();
        workers
            .into_iter()
            .fold(std::f64::MAX, |result, (ratio, quality)| {
                quality_sum += quality as i32;
                heap.push(quality);
                if heap.len() > k {
                    quality_sum -= heap.pop().unwrap() as i32;
                }
                let new_result = quality_sum as f64 * ratio;
                if heap.len() == k && new_result < result {
                    new_result
                } else {
                    result
                }
            })
    }
}

