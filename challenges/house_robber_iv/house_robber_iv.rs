// Change binary search to low/high, inline check fn
impl Solution {
    pub fn min_capability(nums: Vec<i32>, k: i32) -> i32 {
        let (mut low, mut high) = {
            let mut min = nums[0];
            let mut max = nums[0];
            for &num in &nums[1..] {
                if num < min {
                    min = num;
                }
                if num > max {
                    max = num;
                }
            }
            (min, max)
        };
        'outer: while low < high {
            let mid = low + (high - low) / 2;
            let mut count = 0;
            let mut i = 0;
            while i < nums.len() as i32 {
                if nums[i as usize] <= mid {
                    count += 1;
                    i += 2;
                } else {
                    i += 1;
                }
                if count >= k {
                    high = mid;
                    continue 'outer;
                }
            }
            low = mid + 1;
        }
        low
    }
}

