// https://leetcode.com/problems/find-the-power-of-k-size-subarrays-i/

pub struct Solution;

impl Solution {
    pub fn results_array(nums: Vec<i32>, k: i32) -> Vec<i32> {
        assert!(nums.len() <= 500);
        assert!(k >= 1);
        if k == 1 {
            return nums;
        }
        assert!(k <= 500);
        let k = k as u16;
        assert!(k <= nums.len() as u16);
        let mut consecutive_win_len = 1;
        let mut last = -1;
        nums.into_iter()
            .map(|x| {
                if x != last + 1 {
                    consecutive_win_len = 1;
                    last = x;
                    -1
                } else {
                    consecutive_win_len += 1;
                    last = x;
                    if consecutive_win_len >= k {
                        x
                    } else {
                        -1
                    }
                }
            })
            .skip(k as usize - 1)
            .collect()
    }
}

