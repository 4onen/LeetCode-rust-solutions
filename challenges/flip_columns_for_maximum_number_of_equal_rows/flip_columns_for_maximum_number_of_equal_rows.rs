// https://leetcode.com/problems/flip-columns-for-maximum-number-of-equal-rows/

pub struct Solution;

impl Solution {
    pub fn max_equal_rows_after_flips(matrix: Vec<Vec<i32>>) -> i32 {
        let mut pat_map = std::collections::HashMap::<Vec<bool>, u16>::new();
        for row in matrix {
            let first = row[0];
            let pat = row.into_iter().map(|x| x == first).collect();
            *pat_map.entry(pat).or_insert(0) += 1;
        }
        pat_map.into_values().max().unwrap() as i32
    }
}

